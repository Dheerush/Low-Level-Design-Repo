//========================================================= Introduction to Design Patterns =====================================================

/** Introduction 
 *  - Design patterns are reusable solutions to common software design problems.
 *    ==> At their core, Design Patterns are not code; they are blueprints.
 *    ==> They help create clean, scalable, and maintainable code by providing proven design templates.
 *    ==> Think of them as industry-standard templates. You don't copy-paste code; you apply a specific structure to your logic to make it robust.(i.e Pre-Made Solution)
 *    ==> They provide a shared vocabulary for developers. If you tell a teammate, "I'm using a Singleton here," they immediately understand the architecture without looking at a single line of code.
 * 
 * 
 *  - Pattern vs. Algorithm
 *    ==> Algorithm: A set of steps to achieve a result (like sorting a list). It’s like a Recipe.
 *    ==> Design Pattern: A high-level description of an interaction. It’s like a Blueprint for a kitchen.
 * 
 * 
 *  - Why use Design Patterns ?
 *    => Scalability: Patterns ensure that as the app grows, the code doesn't become a "spaghetti" mess.
 *    => Maintenance: Since these are proven solutions, they are naturally easier to debug and modify.
 *    => Efficiency: You aren't "reinventing the wheel." You are using solutions that have been refined by thousands of developers over decades.
 *    => Reliability: These patterns have been tested in high-performance systems for years.
 */



/** Gang of Four (GoF) Overview
 *  -  Erich Gamma, Richard Helm, Ralph Johnson, and John Vlissides wrote the book " Design Patterns: Elements of Reusable Object-Oriented Software".
 *  -  All 4 of them are collectively called as "Gang of Four"
 *     ==> The Gang of Four (GOF) patterns are set of around 23 common software design introduced in the book. 
 *     ==> Most modern frameworks (React, Angular, Spring Boot, etc.) are influenced by these 23 GoF patterns as their foundation.
 */



/** Types of Design Patterns
 *   i).  Creational ; How objects are created 
 *         1. Singleton Pattern
 *         2. Factory Method Pattern
 *         3. Abstract Factory Pattern
 *         4. Builder Pattern
 *         5. Prototype Pattern
 * 
 *       
 *   ii).  Structural ; How objects are composed
 *         1. Adapter Pattern
 *         2. Decorator Pattern
 *         3. Facade Pattern
 *         4. Composite Pattern
 *         5. Proxy Pattern 
 *         6. Bridge Pattern
 *         7. Flyweight Pattern
 *     
 * 
 *   iii). Behavioral ; How objects communicate
 *         1. Observer Pattern
 *         2. Strategy Pattern
 *         3. Command Pattern
 *         4. Chain of Responsibility Pattern
 *         5. Template Method Pattern
 *         6. Iterator Pattern
 *         7. State Pattern
 *         8. Mediator Pattern
 *         9. Memento Pattern
 *        10. Visitor Pattern
 *        11. Interpreter Pattern ; Interpreter is GoF, but low-priority in real-world systems.
 * 
 * 
 */



